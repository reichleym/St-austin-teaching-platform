const missingKeyCache = new Set<string>();

const humanizeKey = (key: string) => {
  const segment = key.split(".").pop() ?? key;
  return segment
    .split(/[_-]/)
    .map((word) => (word ? `${word[0].toUpperCase()}${word.slice(1).toLowerCase()}` : ""))
    .join(" ");
};

const logMissing = (language: Language, key: string) => {
  if (process.env.NODE_ENV === "production") return;
  const cacheKey = `${language}:${key}`;
  if (missingKeyCache.has(cacheKey)) return;
  missingKeyCache.add(cacheKey);
  // eslint-disable-next-line no-console
  console.warn(`[i18n] Missing translation for "${key}" in "${language}". Falling back.`);
};

export function translate(
  language: Language,
  key: string,
  vars?: Record<string, string | number>,
  fallback?: string
) {
  const langTable = translations[language] ?? {};
  const defaultTable = translations[defaultLanguage] ?? {};
  let template = langTable[key];
  if (template === undefined) {
    if (defaultTable[key] !== undefined) {
      logMissing(language, key);
      template = defaultTable[key];
    } else if (fallback !== undefined) {
      logMissing(language, key);
      template = fallback;
    } else {
      logMissing(language, key);
      template = humanizeKey(key);
    }
  }
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, token: string) => String(vars[token] ?? ""));
}
